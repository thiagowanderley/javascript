//OBJETO MATH

let num = 5.25;
let num1 = 10.40;
let num2 = 146;

console.log(Math.floor(num));// Arredonda para baixo
console.log(Math.ceil(num));// Arredonda para cima
console.log(Math.round(num));//Arredonda para o extremo mais proximo
console.log(Math.max(num, num1, num2));// Seleciona o maior número entre os escolhidos
console.log(Math.min(num, num1, num2));// Seleciona o menor número entre os escolhidos
console.log(Math.round(Math.random() * (num1 - num2) + num2));// Apresenta um número aleatorio entre os escolhidos
console.log(num2 ** 0.5)//Descobre a raiz quadrada